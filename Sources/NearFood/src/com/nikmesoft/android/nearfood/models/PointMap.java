package com.nikmesoft.android.nearfood.models;

public class PointMap {
private double x,y;
public PointMap(double x, double y){
	this.x = x;
	this.y = y;
}
public double getX() {
	return x;
}
public void setX(double x) {
	this.x = x;
}
public double getY() {
	return y;
}
public void setY(double y) {
	this.y = y;
}
}
